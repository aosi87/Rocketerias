    public void setComboBoxes(int numComboBoxes){
        JComboBox cBox;
        for(int i = 0; i < numComboBoxes; i++){
            cBox = new JComboBox(this.columns);
            cBox.setVisible(true);
            //this.comboBoxes.add(cBox);
            this.jPanelComboBox.add(cBox);
        }
        this.repaint();
    }

